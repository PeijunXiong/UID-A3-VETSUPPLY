/**
 * Adaptive Width of the design with pixels
 * @param {*} designWidth
 */
export function flexible(designWidth) {
  const baseFontSize = 1; // Base font size, corresponding to the font size of the root element

  var docEl = document.documentElement;
  var dpr = window.devicePixelRatio || 1;

  // adjust body font size
  function setBodyFontSize() {
    if (document.body) {
      document.body.style.fontSize = 16 * dpr + "rem";
    } else {
      document.addEventListener("DOMContentLoaded", setBodyFontSize);
    }
  }
  setBodyFontSize();

  // set 1rem = viewWidth / 10
  function setRemUnit() {
    // const viewportWidth = Math.min(
    //   document.documentElement.clientWidth,
    //   designWidth
    // );
    const viewportWidth = document.documentElement.clientWidth
    const scaleFactor = viewportWidth / designWidth;
    var rem = baseFontSize * scaleFactor;
    docEl.style.fontSize = rem + "px";
  }

  setRemUnit();

  // reset rem unit on page resize
  window.addEventListener("resize", setRemUnit);
  window.addEventListener("pageshow", function (e) {
    if (e.persisted) {
      setRemUnit();
    }
  });

  // detect 0.5px supports
  if (dpr >= 2) {
    var fakeBody = document.createElement("body");
    var testElement = document.createElement("div");
    testElement.style.border = ".5px solid transparent";
    fakeBody.appendChild(testElement);
    docEl.appendChild(fakeBody);
    if (testElement.offsetHeight === 1) {
      docEl.classList.add("hairlines");
    }
    docEl.removeChild(fakeBody);
  }
}
